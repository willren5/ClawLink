export type I18nKey = keyof (typeof messages)['zh'];

export function translate(language: AppLanguage, key: I18nKey): string {
  return messages[language][key] ?? messages.en[key];
}

export function useI18n(): {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  t: (key: I18nKey) => string;
} {
  const language = useAppPreferencesStore((state) => state.language);
  const setLanguage = useAppPreferencesStore((state) => state.setLanguage);

  const t = useMemo(
    () => (key: I18nKey) => translate(language, key),
    [language],
  );

  return { language, setLanguage, t };
}
